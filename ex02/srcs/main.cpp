#include "Fixed.hpp"

int main( void )
{
	Fixed a;
	Fixed const b( Fixed( 5.05f ) * Fixed( 2 ) );
	std::cout << a << std::endl;
	std::cout << ++a << std::endl;
	std::cout << a << std::endl;
	std::cout << a++ << std::endl;
	std::cout << a << std::endl;
	std::cout << b << std::endl;
	std::cout << Fixed::min( a, b ) << std::endl;
	a = a + b;
	std::cout << a << std::endl;

	if (a > b)
		std::cout << "1" << std::endl;
	if (a < b)
		std::cout << "2" << std::endl;
	return 0;
}